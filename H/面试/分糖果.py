import math

# 从标准输入读取糖果的初始数量
x = int(input())
# 初始化操作计数器
count = 0

# 当糖果数量不为1时，继续操作
while x != 1:
    # 特殊情况，当糖果数为3时，直接通过两步操作减至1
    if x == 3:
        count += 2  # 直接加2到计数器（3-1=2，然后2/2=1）
        break
    # 如果当前糖果数是奇数，需要调整糖果数使其成为偶数
    if x % 2 != 0:
        # 选择加1还是减1：如果当前数加1后的一半是偶数，则加1
        if (x + 1) // 2 % 2 == 0:
            x += 1
        else:
            x -= 1  # 否则减1
        count += 1  # 调整操作也算一次操作
    # 糖果数除以2，模拟小明分糖果的过程
    x //= 2
    count += 1  # 分糖果的操作

# 打印总的操作次数
print(count)
